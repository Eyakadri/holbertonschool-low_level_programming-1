#include<stdio.h>
int main(void)
{
	printf("size of a char: %c\n", sizeof(char));
	printf("size of a int: %d\n", sizeof(short int));
	printf("size of a long int: %ld\n", sizeof(long int));
	printf("size of a long long int: %lld\n", sizeof(long double));
	printf("size of a float: %2f\n", sizeof(float));
}
